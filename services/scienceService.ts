/**
 * دالة توليد الأسئلة تضمن:
 * 1. الاختيار العشوائي التام في كل مرة.
 * 2. الفلترة الصارمة حسب المجال المختار.
 * 3. عدم تكرار الأسئلة في الجلسة الواحدة.
 */
export const generateScienceQuestions = (grade: Grade, count: number = 10, outcomeFilter?: string): Question[] => {
  const fullBank = scienceBank[grade] || [];
  
  // دالة الخلط العشوائي (Fisher-Yates Shuffle)
  const shuffle = (array: any[]) => {
    const arr = [...array];
    for (let i = arr.length - 1; i > 0; i--) {
      const j = Math.floor(Math.random() * (i + 1));
      [arr[i], arr[j]] = [arr[j], arr[i]];
    }
    return arr;
  };

  let pool: Omit<Question, 'id'>[] = [];

  if (outcomeFilter && outcomeFilter !== LearningOutcomes.GENERAL) {
    // تصفية صارمة: جلب الأسئلة التي تنتمي للمجال المختار فقط
    pool = fullBank.filter(q => getOutcomeForCategory(grade, q.category || "") === outcomeFilter);
    
    // إذا كان عدد الأسئلة في المجال أقل من المطلوب، نخلط الأسئلة المتاحة
    pool = shuffle(pool);
  } else {
    // في حالة الاختبار الشامل، نخلط البنك كاملاً
    pool = shuffle(fullBank);
  }

  // اختيار العدد المطلوب من الأسئلة المخلوطة عشوائياً
  const finalSelection = pool.slice(0, count);

  return finalSelection.map((q, index) => ({
    ...q,
    id: index + 1
  }));
};

